#using append and insert functions in list

a=[1,2,3]
print("list a= ",a)
x=int(input("enter number to be appended"))
a.append(x)
print(a)

b=[1,2,3]
print("list b= ",b)
y=int(input("enter number to insert"))
z=int(input("enter position for the number"))
b.insert(z,y)
print(b)

    
    
                                  
