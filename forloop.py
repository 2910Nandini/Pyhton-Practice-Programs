#to print values from 1 to 10

a=10          #for single argumented range function
for i in range(a):
    print(i)
print("\n")

n=10           #for doubly argumented range function
for i in range(1,n+1):
    print(i)
print("\n")

n=10           #for triple argumented range function
for i in range(2,n,2):
    print(i)
